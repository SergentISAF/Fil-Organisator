def _config_path():
    d = os.path.join(os.environ.get("APPDATA", os.path.expanduser("~")), "FilOrganisator")
    os.makedirs(d, exist_ok=True)
    return os.path.join(d, "config.json")

def load_config():
    try:
        with open(_config_path(), encoding="utf-8") as f:
            return json.load(f)
    except Exception:
        return {}

def save_config(cfg):
    try:
        with open(_config_path(), "w", encoding="utf-8") as f:
            json.dump(cfg, f)
    except Exception:
        pass

